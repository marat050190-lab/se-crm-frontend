export function getPhoneRegion(phone) {
  if (!phone) return null;
  const digits = phone.replace(/\D/g, '');
  // Берём цифры 2-4 (код после 7)
  const code3 = digits.slice(1, 4);
  const code2 = digits.slice(1, 3);
  return CODES[code3] || CODES[code2] || null;
}
